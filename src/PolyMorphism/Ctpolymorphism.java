public class Ctpolymorphism {
	public void demo1() {
		System.out.println("hello");
	}
	public void demo1(int a) {
		System.out.println(a);
	}
	//in compile time polymorphism mtd declaration binded to its body at the time of compilation based on parameter and argument
    //polymorphism is concept in java which we can perform single action in diff ways
}
